package com.thecraicers.model;

public class Coordinate {

    private Integer x;
    private Integer y;

    public Coordinate() {
        x = 0;
        y = 0;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
